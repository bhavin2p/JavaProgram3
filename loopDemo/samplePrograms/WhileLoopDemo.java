package samplePrograms;

public class WhileLoopDemo {

	public static void main(String[] args) {
		//While loop
		//do while loop
		//for
		//for each

		int num = 10;
		while(num<10) {
			System.out.println(num);
			num++;
		}
		
		System.out.println("Do while");
		int num1 = 10;
		do {
			System.out.println(num1);
			num1++;
		}while(num1<10);
		
		
	}

}
